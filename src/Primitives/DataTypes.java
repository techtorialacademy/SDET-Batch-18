package Primitives;
public class DataTypes {
    public static void main(String[] args) {


        byte number1 = 3;
        System.out.println(number1); // 3

        short number2 = 5;
        System.out.println(number2);// 5

        int number3 = 5;
        System.out.println( number3); //5

        long number4 = 129079L;

        // =======decimal types==== //
        float number5 = 2.3F;
        System.out.println( number5);// 2.3

        float number6 = 2;
        System.out.println(number6);

        double number7 = 4.3;
        System.out.println(number7);


        byte bt1 = 127;
        byte bt2 = -128;

        System.out.println( bt1);// 127

        System.out.println("==================");
        System.out.println( Byte.MAX_VALUE);
        System.out.println(Byte.MIN_VALUE);

        System.out.println(   Integer.MAX_VALUE  ); // int






    }
}
